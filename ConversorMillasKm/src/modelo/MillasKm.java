package modelo;

public class MillasKm {
	
	private double km;
	private double millas;
	public MillasKm(double km, double millas){
		
	}
	public double getKm() {
		return km;
	}
	public void setKm(double km) {
		this.km = km;
	}
	public double getMillas() {
		return millas;
	}
	public void setMillas(double millas) {
		this.millas = millas;
	}
}
